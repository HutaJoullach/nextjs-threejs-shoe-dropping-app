const CreateObjectWizard = () => {
  const { user } = useUser();

  // console.log(user);

  if (!user) return null;

  return (
    <div className="flex w-full gap-3">
      <img
        src={user.profileImageUrl}
        alt="Profile image"
        className="h-10 w-10 rounded-full"
      />
      <input
        placeholder="Type some text"
        className="grow bg-transparent outline-none"
      />
    </div>
  );
};

