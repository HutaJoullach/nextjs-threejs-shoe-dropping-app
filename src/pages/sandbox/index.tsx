type ObjectWithUser = RouterOutputs["objects"]["getAll"][number];
const CanvasContainer = (props: ObjectWithUser) => {
  const { object, author } = props;
  return (
    <div key={object.id} className="flex gap-2 p-2">
      <span>{object.objectType}</span>
      <div className="flex items-center gap-1 rounded-md bg-zinc-700 p-1">
        <Image
          src={author.profileImageUrl}
          className="h-7 w-7 rounded-full"
          alt={`@${author.username}'s profile pic`}
          width={56}
          height={56}
        />
        <div className="flex flex-col text-xs font-bold text-slate-300">
          <span>{`@${author.username}`}</span>
          <span className="flex justify-center font-thin">{`${dayjs(
            object.createdAt
          ).fromNow()}`}</span>
        </div>
      </div>
    </div>
  );
};

const Board = () => {
  const { data, isLoading: objectsLoading } = api.objects.getAll.useQuery();

  if (objectsLoading) return <LoadingPage />;

  if (!data) return <div>Something went wrong</div>;

  return (
    <div>
      {data.map((objectData) => (
        <CanvasContainer {...objectData} key={objectData.object.id} />
      ))}
    </div>
  );
};

const Sandbox: NextPage = () => {
  const { isLoaded: userLoaded, isSignedIn } = useUser();

  // Start fetching data
  api.objects.getAll.useQuery();

  if (!userLoaded) return <div />;

  return (
    <>
      <Head>
        <title>Sandbox</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/alphabet.svg" />
      </Head>
      {/* <PageLayout></PageLayout> */}
      <div className="h-full w-full md:max-w-7xl">
        <div className="flex border-b border-slate-400 p-4">
          {!isSignedIn && (
            <div className="flex justify-center">
              <SignInButton />
            </div>
          )}
          {!!isSignedIn && (
            <div className="flex justify-center">
              <CreateObjectWizard />
              <SignOutButton />
            </div>
          )}
        </div>
        <Board />
      </div>
    </>
  );
};

export default Sandbox;
